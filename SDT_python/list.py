# list, array, collection is same

# i  = [0,  1,  2,  3,  4,  5]
nums = [45, 56, 76, 12, 43, 98]
#-i  = [-6, -5, -4, -3, -2, -1]

# divide the array => list(start : end : step)
# print(nums[2:5])
print(nums[2 : 5 : 2])
# reverse
print(nums[4 : 1 : -2])





# shortcut to copy a list
print(nums[:])





# shortcut to reverse print a list
print(nums[::-1])